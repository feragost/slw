package crawler;

public class MapAuthTime {
	
	private long auth;
	private long timeMillis;
	
	public MapAuthTime(long auth, long timeMillis){
		this.auth = auth;
		this.timeMillis = timeMillis;
	}

	public long getAuth() {
		return auth;
	}

	public long getTimeMillis() {
		return timeMillis;
	}
	
	

}
